# Desafio 006 - Crie um programa que leia um numero e mostre seu doubro, seu triplo e sua raiz quadrada
nO = int(input('Informe um número inteiro: '))
nD = nO*2
nT = nO*3
nR2 = nO**(1/2)
print(f'O dobro de {nO} é igual á {nD}.')
print(f'O triplo de {nO} é igual á {nT}.')
print(f'A raiz quadrada de {nO} é igual á {nR2}.')